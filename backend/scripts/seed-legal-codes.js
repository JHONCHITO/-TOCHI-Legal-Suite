async function seedLegalCodes() {
  try {
    console.log("Conectando a MongoDB...");
    await mongoose.connect(MONGODB_URI);
    console.log("Conectado a MongoDB");

    console.log("Eliminando codigos existentes...");
    await LegalCode.deleteMany({});

    console.log("Insertando codigos legales...");
    const result = await LegalCode.insertMany(codigosLegales);
    console.log(`Se insertaron ${result.length} codigos legales`);

    console.log("Proceso completado exitosamente");
  } catch (error) {
    console.error("Error:", error);
  } finally {
    await mongoose.disconnect();
    console.log("Desconectado de MongoDB");
  }
}

seedLegalCodes();
